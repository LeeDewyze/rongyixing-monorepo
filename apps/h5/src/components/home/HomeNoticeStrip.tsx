function NoticeText({ title }: { title: string }) {
  return (
    <span className="min-w-0 flex-1 truncate text-[12px] font-medium leading-none text-[#EF6000]/80">
      {title}
    </span>
  );
}

export function HomeNoticeStrip({ notices = [], onClick }: HomeNoticeStripProps) {
  const [activeIndex, setActiveIndex] = useState(0);

  const validNotices = useMemo(
    () => notices.filter((notice) => Boolean(notice?.Title)),
    [notices],
  );

  useEffect(() => {
    if (activeIndex >= validNotices.length) {
      setActiveIndex(0);
    }
  }, [activeIndex, validNotices.length]);

  useEffect(() => {
    if (validNotices.length <= 1) return;
    const timer = window.setInterval(() => {
      setActiveIndex((value) => (value + 1) % validNotices.length);
    }, 3500);
    return () => window.clearInterval(timer);
  }, [validNotices.length]);

  if (validNotices.length === 0) return null;

  return (
    <button
      type="button"
      className="flex h-[24px] w-full items-center rounded-full px-3 text-left shadow-[0_2px_8px_rgba(0,0,0,0.04)] active:opacity-90"
      style={{ backgroundColor: "rgba(248, 187, 64, 0.1)" }}
      onClick={onClick}
    >
      <span className="mr-3 flex shrink-0 items-center justify-center text-[#EF6000]/80">
        <AnnouncementIcon />
      </span>
      <span className="relative h-full min-w-0 flex-1 overflow-hidden">
        {validNotices.map((notice, index) => (
          <span
            key={String(notice.Id ?? `${notice.Title}-${index}`)}
            className={`absolute inset-0 flex items-center transition-all duration-300 ${
              index === activeIndex
                ? "translate-y-0 opacity-100"
                : index < activeIndex
                  ? "-translate-y-2 opacity-0"
                  : "translate-y-2 opacity-0"
            }`}
            aria-hidden={index !== activeIndex}
          >
            <NoticeText title={notice.Title} />
          </span>
        ))}
      </span>
      <svg viewBox="0 0 16 16" className="ml-2 size-4 shrink-0 text-[#EF6000]/80" aria-hidden>
        <path
          d="M6 4.5L9.5 8L6 11.5"
          fill="none"
          stroke="currentColor"
          strokeWidth="1.5"
          strokeLinecap="round"
          strokeLinejoin="round"
        />
      </svg>
    </button>
  );
}
